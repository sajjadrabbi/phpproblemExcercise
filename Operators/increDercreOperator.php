<?php

    $counter = 10;
    $counter = $counter + 1;
    echo $counter . PHP_EOL;

    //++
    //post increment
    echo $counter++ . PHP_EOL;
    echo $counter . PHP_EOL;

    //pre increment operator
    echo ++$counter . PHP_EOL;
    echo $counter . PHP_EOL;

    //-- operator
    $counter = 10;
    $counter  -= 1;
    echo $counter . PHP_EOL;

    //++
    //post increment
    echo $counter-- . PHP_EOL;
    echo $counter . PHP_EOL;

    //pre increment operator
    echo --$counter . PHP_EOL;
    echo $counter . PHP_EOL;


?>